import { Router } from "express";
import {
  updateRoomController,
  deleteRoomController,
  assignTenantController,
  removeTenantController,
  getAllRoomsController,
  getRoomByIdController,
  getOccupiedRoomsController,
} from "../controllers/room.controller";
import { validateDto } from "../middlewares/validate.middleware";
import { UpdateRoomDto, AssignTenantDto } from "../dtos/room.dto";
import { authMiddleware } from "../middlewares/auth.middleware";
import { requireRole } from "../middlewares/role.middleware";
import { ROLE } from "../utils/app.constants";

const router = Router();

router.get(
  "/",
  authMiddleware,
  requireRole([ROLE.OWNER]),
  getAllRoomsController,
);

router.put(
  "/:id",
  authMiddleware,
  requireRole([ROLE.OWNER]),
  validateDto(UpdateRoomDto),
  updateRoomController,
);

router.delete(
  "/:id",
  authMiddleware,
  requireRole([ROLE.OWNER]),
  deleteRoomController,
);

router.post(
  "/:id/assign-tenant",
  authMiddleware,
  requireRole([ROLE.OWNER]),
  validateDto(AssignTenantDto),
  assignTenantController,
);

router.post(
  "/:id/remove-tenant",
  authMiddleware,
  requireRole([ROLE.OWNER]),
  removeTenantController,
);

router.get(
  "/occupied",
  authMiddleware,
  requireRole([ROLE.OWNER]),
  getOccupiedRoomsController,
);

router.get(
  "/:id",
  authMiddleware,
  requireRole([ROLE.OWNER, ROLE.TENANT]),
  getRoomByIdController,
);

export default router;
